#here are some notes on the simple navigation commands on a linux bash

#_______________________________________________________________________#

whoami #tells you your status as a user

pwd #stands for "print working directory" and tells me where I am within the file directory (where)
ls  #stands for "list" and lets me know what my files are from where I currently am in the file directory. (what)
ls -l # a more detailed list of the files in the directory (what)

cd # "change directory" allows me to navigate the file directory (navigation)
cd .. #travel 'back' in a file directory (navigation)

date #gives me a timestamp of the machine (when)

